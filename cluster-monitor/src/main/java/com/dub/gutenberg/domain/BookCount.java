package com.dub.gutenberg.domain;

public class BookCount {
	
	private String bookId;
	
	private int count;

	public String getBookId() {
		return bookId;
	}

	public void setBookId(String bookId) {
		this.bookId = bookId;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}	
}
